package com.netsol.neo4j.vo;

public class CompanyVo {
	
	private Long id;
	
	
	private String name;

	public CompanyVo() {
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

}
